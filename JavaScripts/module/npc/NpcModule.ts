    private static readonly NPC_PREFAB_GUID = "EF576168418ED3113B4C43800A36A451";

    public static async NpcPrefabFactory(
        config: INpcElement) {
        if (!config) {
            Log4Ts.error(NpcModuleC, `invalid config when generate npc.`);
        }
        if (!config.position) {
            Log4Ts.error(NpcModuleC, `generate item param location invalid.`);
            return null;
        }

        const obj = await GameObject.asyncSpawn(
            NpcModuleC.NPC_PREFAB_GUID,
            {
                replicates: false,
                transform: new Transform(
                    config.position,
                    new Rotation(config.rotation),
                    Vector.one),
            },
        );

        GToolkit.getFirstScript(obj, NpcTrigger)?.init(config.id);
        GToolkit.getFirstScript(obj, NpcBehavior)?.init(config);

        return obj;
    }

