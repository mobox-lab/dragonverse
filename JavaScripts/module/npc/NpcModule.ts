    private initAllNpc() {
        GameConfig
            .Npc
            .getAllElement()
            .forEach(
                config => {
                    Log4Ts.log(NpcModuleC, `try generate npc. ${config.id}`);
                    NpcModuleC.NpcPrefabFactory(config).then(
                        (value) => {
                            Log4Ts.log(NpcModuleC,
                                `generate npc success.`,
                                `id: ${config.id}`,
                                `name: ${GameConfig.Character.getElement(config.characterId)?.name ?? "null"}`);
                        },
                    );
                },
            );
    }

