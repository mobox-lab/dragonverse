import Log4Ts from "mw-log4ts";
import GToolkit from "gtoolkit";
import GameObject = mw.GameObject;
import { EventDefine } from "../../const/EventDefine";

export enum FirePuzzleBlockTypes {
    /**
     * 空置.
     */
    Null,
    /**
     * 岩浆.
     */
    Magma,
    /**
     * 水.
     */
    Water,
}

/**
 * 火炎地狱 谜题方块 Behavior.
 *
 * ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟
 * ⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄
 * ⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄
 * ⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄
 * ⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄
 * @author LviatYi
 * @font JetBrainsMono Nerd Font Mono https://github.com/ryanoasis/nerd-fonts/releases/download/v3.0.2/JetBrainsMono.zip
 * @fallbackFont Sarasa Mono SC https://github.com/be5invis/Sarasa-Gothic/releases/download/v0.41.6/sarasa-gothic-ttf-0.41.6.7z
 */
@Component
export default class FirePuzzleBlock extends mw.Script {
//#region Constant
    private static readonly BLOCK_MAGMA_MATERIAL_ASSET_ID = "6EAB7AFB42853BE6F1B1989C46D3ED77";

    private static readonly BLOCK_WATER_MATERIAL_ASSET_ID = "197999";

    private static readonly GAME_OBJECT_MESH_NAME = "mesh";

    private static readonly GAME_OBJECT_TRIGGER_NAME = "trigger";

//#endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//#region Member

    @Property({displayName: "方块类型", group: "Config | block", enumType: FirePuzzleBlockTypes})
    public originBlockType: FirePuzzleBlockTypes = FirePuzzleBlockTypes.Water;

    @Property({displayName: "湿润 Buff 持续时长 s", group: "Config | buff"})
    public wetBuffDuration: number = 5;

    @Property({displayName: "爆炸冲量力", group: "Config | buff"})
    public force: number = 1000;

    private _clientBlockType: FirePuzzleBlockTypes = FirePuzzleBlockTypes.Null;

    private _meshObj: Model;

    private _trigger: Trigger;


//#endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//region MetaWorld Event
    protected onStart(): void {
        super.onStart();

//region Member init
        if (SystemUtil.isClient()) {
            this._meshObj = GToolkit.getFirstGameObject(this.gameObject, FirePuzzleBlock.GAME_OBJECT_MESH_NAME) as Model;
            this.switchType(this.originBlockType);
            if (!this._meshObj) {
                Log4Ts.error(FirePuzzleBlock, `client there is no mesh object in this block.`);
                return;
            }
        } else if (SystemUtil.isServer()) {
            this._trigger = GToolkit.getFirstGameObject(this.gameObject, FirePuzzleBlock.GAME_OBJECT_TRIGGER_NAME) as Trigger;
            if (!this._trigger) {
                Log4Ts.error(FirePuzzleBlock, `there is no trigger under this game object.`);
            } else {
                this._trigger.onEnter.add(this.onObjectEnter);
                this._trigger.onLeave.add(this.onObjectLeave);
            }
        }
    }


//endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//region Widget bind
//endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//region Event subscribe
//endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

    protected onDestroy(): void {
        super.onDestroy();
    }

//endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//#region Method
    @RemoteFunction(Client)
    public switchType(type: FirePuzzleBlockTypes) {
        if (this._clientBlockType === type) return;

        let materialAssetId: string;
        switch (type) {
            case FirePuzzleBlockTypes.Magma:
                materialAssetId = FirePuzzleBlock.BLOCK_MAGMA_MATERIAL_ASSET_ID;
                break;
            case FirePuzzleBlockTypes.Water:
                materialAssetId = FirePuzzleBlock.BLOCK_WATER_MATERIAL_ASSET_ID;
                break;
            default:
                Log4Ts.error(FirePuzzleBlock, `client switch type error. type: ${type} is not supported.`);
                break;
        }
        this._meshObj.setMaterial(materialAssetId);
        Log4Ts.log(FirePuzzleBlock, `client change block type success. current type: ${type}`);
    }

//#endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄

//region Event Callback
    private onObjectEnter = (obj: GameObject): void => {
        if (GToolkit.isCharacter(obj)) {
            Event.dispatchToClient(
                obj.player,
                EventDefine.PlayerEnterFirePuzzleBlock,
                this.gameObject.gameObjectId,
                this.force,
                this.wetBuffDuration,
            );
        }
    };

    private onObjectLeave = (obj: GameObject): void => {
        if (GToolkit.isCharacter(obj)) {
            Event.dispatchToClient(
                obj.player,
                EventDefine.PlayerLeaveFirePuzzleBlock,
                this.gameObject.gameObjectId,
                this.wetBuffDuration);
        }
    };


//endregion ⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠐⠒⠒⠒⠒⠚⠛⣿⡟⠄⠄⢠⠄⠄⠄⡄⠄⠄⣠⡶⠶⣶⠶⠶⠂⣠⣶⣶⠂⠄⣸⡿⠄⠄⢀⣿⠇⠄⣰⡿⣠⡾⠋⠄⣼⡟⠄⣠⡾⠋⣾⠏⠄⢰⣿⠁⠄⠄⣾⡏⠄⠠⠿⠿⠋⠠⠶⠶⠿⠶⠾⠋⠄⠽⠟⠄⠄⠄⠃⠄⠄⣼⣿⣤⡤⠤⠤⠤⠤⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄⠄
}
