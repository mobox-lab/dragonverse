    //#region Method

    /**
     * 请求引导.
     * @param {number} stepId
     */
    public requestGuide(stepId: number): void {
        if (this.manager.isComplete(stepId)) {
            Log4Ts.log(GuideModuleC, `guide ${stepId} is already complete.`);
            return;
        }
        this.manager.isComplete(stepId) && this.manager.requestNext(stepId);
    }

